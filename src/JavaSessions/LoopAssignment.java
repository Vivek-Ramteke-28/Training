package JavaSessions;

public class LoopAssignment {

	public static void main(String[] args) {
		
		
		for(int a=10; a>=1; a--)
		{
			System.out.println(a);
		}
		
		System.out.println("------------");
		
		int b=10;
		while(b>=1)
		{
			System.out.println(b);
			b--;
		}
		System.out.println("------------");
		
			
		int n = 10;
		do{
			System.out.println(n);
			n--;
		}
		while(n>=1);
		
		System.out.println("------------");
		
		String str = "Hello World";
		int c=1;
		while(c<=10)
		{
			System.out.println(str);
			c++;
		}

	}

}
